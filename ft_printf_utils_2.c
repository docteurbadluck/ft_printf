#include "ft_printf.h"

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int	ft_strlen(const char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return (i);
}

void	ft_putstr( char *s)
{
	int	size;

	if (!s)
		return ;
	size = ft_strlen(s);
	write(1, s, size);
	g_count += size;
}
